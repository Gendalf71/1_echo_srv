import socket  # Импортируем модуль для работы с сокетами
from time import sleep  # Импортируем sleep, хотя в данном коде он не используется

# Создаем TCP-сокет
sock = socket.socket()

# Устанавливаем блокирующий режим (по умолчанию сокеты работают в блокирующем режиме)
sock.setblocking(1)

# Подключаемся к серверу, который работает на локальном компьютере (localhost) по порту 9090
sock.connect(('localhost', 9090))

# Запрашиваем у пользователя ввод строки для отправки на сервер
msg = input("Input string:")

# Преобразуем строку в байты и отправляем на сервер
sock.send(msg.encode())

# Принимаем ответ от сервера (до 1024 байт)
data = sock.recv(1024)

# Закрываем соединение, так как работа клиента завершена
sock.close()

# Декодируем полученные байты обратно в строку и выводим результат на экран
print(data.decode())
